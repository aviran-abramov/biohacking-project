        Lentils are a one of the healthiest foods you can add to your diet. Packed with complex carbs, proteins, fibers, low in fat, and these legumes are also low in fat and calories. Plus, they bring a boost of calcium, phosphorus, iron, and B vitamins, making them one of the best foods, even for bodybuilders.
    </p>

